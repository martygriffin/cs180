package com.order;

import java.util.List;
import java.util.Set;

import com.hibernate.Custom;
import com.hibernate.Item;
import com.hibernate.Order;

public class OrderForm
{
	private List<Order> order_list;
	private List<Item> item_list;
	private Order current_order;
	private Set<Custom>customList;
	private double total;
	

	public void setOrder_list(List<Order> order_list) {
		this.order_list = order_list;
	
	}

	public List<Order> getOrder_list() {
		return order_list;
	}

	public List<Item> getItem_list() {
		return item_list;
	}

	public void setItem_list(List<Item> item_list) {
		this.item_list = item_list;
	}

	public Order getCurrent_order() {
		return current_order;
	}

	public void setCurrent_order(Order current_order) {
		this.current_order = current_order;
	}

	public double getTotal() {
		return total;
	}

	public void setTotal(double total) {
		this.total = total;
	}

	public void setCustomList(Set<Custom> customList) {
		this.customList = customList;
	}

	public Set<Custom> getCustomList() {
		return customList;
	}

	
}