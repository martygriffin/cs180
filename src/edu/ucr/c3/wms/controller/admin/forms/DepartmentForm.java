package edu.ucr.c3.wms.controller.admin.forms;

import java.util.List;

import edu.ucr.c3.wms.helper.validators.BaseValidator;
import edu.ucr.c3.wms.domain.College;

public class DepartmentForm {
	
	private int deptId = BaseValidator.NOT_SELECTED;
	private int colId = BaseValidator.NOT_SELECTED;
	private String servName;
	private String name;
	private String url;
	private String authKey;
	private List<College> cols;
	
	public void setColId(int colId) {
		this.colId = colId;
	}
	public int getColId() {
		return colId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getDeptId() {
		return deptId;
	}
	public void setDeptId(int deptId) {
		this.deptId = deptId;
	}
	public String getServName() {
		return servName;
	}
	public void setServName(String servName) {
		this.servName = servName;
	}
	public String getAuthKey() {
		return authKey;
	}
	public void setAuthKey(String authKey) {
		this.authKey = authKey;
	}
	public List<College> getCols() {
		return cols;
	}
	public void setCols(List<College> cols) {
		this.cols = cols;
	}

}
