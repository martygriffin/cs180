package edu.ucr.c3.wms.controller.manager.forms;

import java.util.Date;
import java.util.List;

import edu.ucr.c3.wms.helper.validators.BaseValidator;
import edu.ucr.c3.wms.domain.AcademicTerm;

public class SessionForm {
	private int sessId = BaseValidator.NOT_SELECTED;
	private Short year = null;
	private int termId = BaseValidator.NOT_SELECTED;
	private String termName;
	private int workshopId = BaseValidator.NOT_SELECTED;
	private String name;
	private Date startDate;
	private Date endDate;
	private String time;
	private String location;
	private Integer maxCap = null;
	private Short daysEarly = null;
	private String comments;
	private List<AcademicTerm> terms;
	
	public int getSessId() {
		return sessId;
	}
	public void setSessId(int sessId) {
		this.sessId = sessId;
	}
	public Short getYear() {
		return year;
	}
	public void setYear(Short year) {
		this.year = year;
	}
	public int getTermId() {
		return termId;
	}
	public void setTermId(int termId) {
		this.termId = termId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getStartDate() {
		return startDate;
	}
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getMaxCap() {
		return maxCap;
	}
	public void setMaxCap(int maxCap) {
		this.maxCap = maxCap;
	}
	public short getDaysEarly() {
		return daysEarly;
	}
	public void setDaysEarly(short daysEarly) {
		this.daysEarly = daysEarly;
	}
	public List<AcademicTerm> getTerms() {
		return terms;
	}
	public void setTerms(List<AcademicTerm> terms) {
		this.terms = terms;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public String getComments() {
		return comments;
	}
	public int getWorkshopId() {
		return workshopId;
	}
	public void setWorkshopId(int workshopId) {
		this.workshopId = workshopId;
	}
	public void setTermName(String termName) {
		this.termName = termName;
	}
	public String getTermName() {
		return termName;
	}
}
