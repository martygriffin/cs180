package edu.ucr.c3.phr.domain;

import java.util.Date;

/**
 * Notes entity. @author MyEclipse Persistence Tools
 */

public class Notes implements java.io.Serializable {

	// Fields

	private Integer id;
	private String note;
	private Date date;
	private Short active;

	// Constructors

	/** default constructor */
	public Notes() {
	}

	/** full constructor */
	public Notes(String note, Date date, Short active) {
		this.note = note;
		this.date = date;
		this.active = active;
	}

	// Property accessors

	public Integer getId() {
		return this.id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNote() {
		return this.note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public Date getDate() {
		return this.date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public Short getActive() {
		return this.active;
	}

	public void setActive(Short active) {
		this.active = active;
	}

}